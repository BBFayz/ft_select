#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	char *mem;

	mem = s1;
	while (*mem)
		mem++;
	while (*s2)
		*(mem++) = *(s2++);
	*mem = '\0';
	return (s1);
}
